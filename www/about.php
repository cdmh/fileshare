<h1>Real-time file sharing</h1>
<p>pyf.li is a really simple to use platform to instantly share files across the internet. With one click in the web browser interface, any user can share any file with other users. The file is uploaded to a server and a link to download the same file is given immediately. Here’s the clever bit; files can be downloaded before the upload has completed, making pyf.li a true real-time file sharing solution.</p>
<p>We wanted to make the service as easy to use as possible. Once the file upload has begun, a download link is immediately available. Our web interface makes it easy to copy the download link to your clipboard, and can also create an email with the link embedded.</p>
<h2>Real-time file sharing on Twitter</h2>
<p>An additional feature on pyf.li is a really simple Tweet and Share file-sharing for Twitter. Write a tweet on the pyf.li website, add a file and the tweet is sent straight away with a link to the file download, even before the file is uploaded. Brilliant!</p>
